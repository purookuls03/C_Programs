int sum(int num1,int num2)
{
	return num1+num2;

}

int sub(int num1,int num2)
{
	return num1+num2;

}


int multi(int num1,int num2)
{
	return num1+num2;

}


int div(int num1,int num2)
{
	return num1+num2;

}



int max(int num1,int num2)
{
		if (num1>num2)
			return num1;
		else
			return num2;
}


int min(int num1,int num2)
{
		if (num1>num2)
			return num2;
		else
			return num1;
}

